// --- test helpers, package-scoped to avoid colliding with the local
// closures of the same shape already defined inside
// groth16_zk_acceptance_test.go's own test functions. ---

func submitGroth16RealProof(t *testing.T, runner *avm.Runner, res *compiler.Result, deployer sdk.AccAddress, state avm.Storage, vk, proof, publicInputs []byte) avm.Storage {
	t.Helper()
	verifyCodec := res.MessageBodies["VerifyGroth16Proof"]
	require.NotEmpty(t, verifyCodec.Fields, "VerifyGroth16Proof codec must be registered")
	verifyOpcode := res.MessageBodyOpcodes["VerifyGroth16Proof"]

	body := mustCodecBody(t, verifyCodec, map[string]any{"vk": vk, "proof": proof, "publicInputs": publicInputs})
	exec, err := runner.Run(res.Module, state, avm.RuntimeContext{
		Entry:           avm.EntryReceiveInternal,
		ContractAddress: deployer,
		GasLimit:        50_000_000,
		Message: async.MessageEnvelope{
			Opcode:   verifyOpcode,
			QueryID:  uint64(verifyOpcode),
			Body:     body,
			GasLimit: 50_000_000,
		},
	})
	require.NoError(t, err)
	require.Equal(t, async.ResultOK, exec.ResultCode, "VerifyGroth16Proof must never trap on malformed/adversarial input")
	return exec.State
}

