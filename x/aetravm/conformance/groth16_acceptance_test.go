// --- Phase D Groth16-over-BN254 REAL differential golden vectors.
//
// This is tier 2 of docs/architecture/avm-phase-d-zk-design.md's Section 6
// three-tier test matrix: a REAL Groth16 proof, generated OFFLINE by gnark's
// own R1CS frontend + Groth16 prover (github.com/consensys/gnark, NOT
// gnark-crypto -- see the design doc's own "gnark-crypto only, not the full
// gnark module" scope call), for two toy circuits:
//
//   - "square": prove knowledge of X such that X*X == Y (Y public). n=1
//     public input.
//   - "twoPublic": prove knowledge of X, Z such that X*X == Y1 AND X*Z == Y2
//     (Y1, Y2 public). n=2 public inputs -- exercises groth16_stdlib.atlx's
//     vk_x accumulation loop over >=2 terms, unlike
//     groth16_zk_acceptance_test.go's synthetic n<=1 vectors.
//
// This is the load-bearing test the design doc's blocker 3 asked for: it
// proves the hand-rolled groth16_stdlib.atlx verify equation is correct
// against a REAL proof system's output (gnark's own Verify() independently
// accepted both proofs before these bytes were captured -- see the
// generator's own sanity check), not just internally self-consistent against
// synthetic, equation-satisfying-by-construction material the way
// groth16_zk_acceptance_test.go's vectors are.
//
// Fixture provenance: the byte literals below were generated ONCE by a
// throwaway, non-consensus, non-committed Go program (outside this module,
// requiring the full gnark module -- deliberately never added to this
// repo's go.mod/go.sum, per the design doc's scope call) that:
//  1. built the two circuits above via gnark's frontend.Compile +
//     r1cs.NewBuilder,
//  2. ran groth16.Setup / groth16.Prove,
//  3. called gnark's OWN groth16.Verify(proof, vk, publicWitness) and
//     confirmed it returned nil (accepted) before ever emitting these bytes,
//  4. re-packed vk.G1.Alpha/vk.G2.{Beta,Gamma,Delta}/vk.G1.K,
//     proof.{Ar,Bs,Krs}, and the public witness vector into
//     groth16_stdlib.atlx's documented byte ABI (64-byte G1 / 128-byte G2 /
//     32-byte BE scalars, vk = alpha||beta||gamma||delta||IC[0..n]) using
//     the exact same X.Bytes()||Y.Bytes() / X.A0||X.A1||Y.A0||Y.A1 encoding
//     bn254DecodeG1/bn254DecodeG2 (avm.go) expect -- confirmed correct here
//     by these vectors round-tripping through the real VM to a true verdict
//     below, not merely asserted.
//
// The gnark<->groth16_stdlib.atlx equation correspondence that makes this
// direct byte repacking valid (not just "looks similar"): gnark's own
// verify.go computes e(alpha,beta) = e(Ar,Bs) * e(kSum,-gamma) *
// e(Krs,-delta) (kSum = vk.G1.K[0] + sum_i publicWitness[i]*vk.G1.K[i+1]),
// which rearranges to e(-Ar,Bs) * e(alpha,beta) * e(kSum,gamma) *
// e(Krs,delta) == 1 -- EXACTLY groth16_stdlib.atlx's own documented
// verify equation (proof.A=Ar, proof.B=Bs, proof.C=Krs, vk_x=kSum),
// confirming this isn't a coincidental byte-shape match: it is the identical
// algebraic statement.

