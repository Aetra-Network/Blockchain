// TestAcceptanceGroth16RealProofAdversarial covers tier 3 of the design
// doc's Section 6 matrix using the REAL golden vectors above (not synthetic
// material), proving the whole pipeline soft-fails to false -- never traps
// -- on adversarial input built from an otherwise-valid, real proof.
func TestAcceptanceGroth16RealProofAdversarial(t *testing.T) {
	deployer := testAddress(0xB3)
	res := compileExampleFile(t, filepath.Join("zk", "groth16_stdlib.atlx"), compiler.Options{
		DeployerAddress: addressing.FormatAccAddress(deployer),
	})
	require.NoError(t, avm.VerifyInterface(res.Module, res.Manifest))
	runner, err := avm.NewRunner(avm.DefaultParams())
	require.NoError(t, err)

	// (a) A single-bit-flipped proof, derived from the real "square"
	// vector, must verify false -- not trap.
	t.Run("single bit flipped proof soft-fails false", func(t *testing.T) {
		state := submitGroth16RealProof(t, runner, res, deployer, avm.Storage{}, squareVKCopy(), squareProofBitFlipped, squarePublicInputs)
		require.Equal(t, uint64(0), readGroth16VerifiedFlag(t, runner, res, state), "a single-bit-flipped real proof must not verify")
	})

	// (b) A G2 point that IS on the BN254 twist curve but is NOT in the
	// correct r-order subgroup, substituted in as proof.B, must verify
	// false -- proving IsInSubGroup() is actually wired into the decode
	// path this stdlib routes through (bn254DecodeG2 in avm.go), not just
	// present in isolated opcode tests. Constructed via gnark-crypto's own
	// exported MapToCurve2 (the pre-cofactor-clearing step of its SVDW
	// hash-to-curve map -- explicitly documented by gnark-crypto itself as
	// NOT performing cofactor clearing), which lands on-curve but, since
	// BN254's G2 cofactor is astronomically large, off-subgroup with
	// overwhelming probability for any fixed input -- confirmed for this
	// exact input below before it's used adversarially.
	t.Run("on-curve out-of-subgroup G2 point as proof.B soft-fails false", func(t *testing.T) {
		var u bn254.E2
		u.A0.SetUint64(12345)
		badB := bn254.MapToCurve2(&u)
		require.True(t, badB.IsOnCurve(), "sanity: MapToCurve2 output must be on-curve")
		require.False(t, badB.IsInSubGroup(), "sanity: MapToCurve2 output must NOT be in the r-order subgroup (the actual adversarial condition under test)")

		badProof := append([]byte(nil), squareProof...)
		copy(badProof[64:192], encodeG2(badB))

		state := submitGroth16RealProof(t, runner, res, deployer, avm.Storage{}, squareVKCopy(), badProof, squarePublicInputs)
		require.Equal(t, uint64(0), readGroth16VerifiedFlag(t, runner, res, state), "an on-curve-but-out-of-subgroup proof.B must not verify")
	})

	// (c) Malformed lengths derived from the real vk/proof (not just
	// synthetic material) must soft-fail false, never trap: a vk shape
	// that isn't 448+64*(n+1) for any integer n, and a proof shorter than
	// PROOF_LEN.
	t.Run("malformed vk/proof length soft-fails false", func(t *testing.T) {
		t.Run("vk one byte short of a valid shape", func(t *testing.T) {
			badVK := squareVKCopy()
			badVK = badVK[:len(badVK)-1]
			state := submitGroth16RealProof(t, runner, res, deployer, avm.Storage{}, badVK, squareProof, squarePublicInputs)
			require.Equal(t, uint64(0), readGroth16VerifiedFlag(t, runner, res, state))
		})
		t.Run("proof truncated", func(t *testing.T) {
			state := submitGroth16RealProof(t, runner, res, deployer, avm.Storage{}, squareVKCopy(), squareProof[:32], squarePublicInputs)
			require.Equal(t, uint64(0), readGroth16VerifiedFlag(t, runner, res, state))
		})
	})

	// (d) publicInputs count mismatched against vk's own derived IC-array
	// length (n=1 for the real "square" vk) must soft-fail false, never
	// trap -- both too few (0) and too many (2) public inputs.
	t.Run("publicInputs count mismatched against vk's IC length soft-fails false", func(t *testing.T) {
		t.Run("zero public inputs against an n=1 vk", func(t *testing.T) {
			state := submitGroth16RealProof(t, runner, res, deployer, avm.Storage{}, squareVKCopy(), squareProof, nil)
			require.Equal(t, uint64(0), readGroth16VerifiedFlag(t, runner, res, state))
		})
		t.Run("two public inputs against an n=1 vk", func(t *testing.T) {
			tooMany := append(append([]byte(nil), squarePublicInputs...), scalarBE32(1)...)
			state := submitGroth16RealProof(t, runner, res, deployer, avm.Storage{}, squareVKCopy(), squareProof, tooMany)
			require.Equal(t, uint64(0), readGroth16VerifiedFlag(t, runner, res, state))
		})
		t.Run("real n=2 publicInputs against the n=1 square vk", func(t *testing.T) {
			// Cross-vector mismatch: a real, individually-valid publicInputs
			// blob from the OTHER (n=2) circuit, fed against the n=1 "square"
			// vk/proof pairing -- proves the length check is against the vk's
			// own derived n, not merely "any real-looking blob passes".
			state := submitGroth16RealProof(t, runner, res, deployer, avm.Storage{}, squareVKCopy(), squareProof, twoPublicPublicInputs)
			require.Equal(t, uint64(0), readGroth16VerifiedFlag(t, runner, res, state))
		})
	})
}
